public class ShepToken {
    private String token = null, idName = null;
    private Integer tokenVal = null, intVal = null;

    public String getToken() {
        return token;
    }
    public void setToken(String token) {
        this.token = token;
    }
    public String getIdName() {
        return idName;
    }
    public void setIdName(String idName) {
        this.idName = idName;
    }
    public Integer getTokenVal() {
        return tokenVal;
    }
    public void setTokenVal(int tokenVal) {
        this.tokenVal = tokenVal;
    }
    public Integer getIntVal() {
        return intVal;
    }
    public void setIntVal(int intVal) {
        this.intVal = intVal;
    }
}
